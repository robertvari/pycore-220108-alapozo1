# use input() to get data from user
my_name = input("What is your name?")
my_age = input("Your age?")
my_email = input("You email?")

print(f"Hello, my name is {my_name}. My age is {my_age}. My email is: {my_email}")

# old style string formating
print("Hello, my name is {}. My age is {}. My email is: {}".format(my_name, my_age, my_email))